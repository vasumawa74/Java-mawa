public class ifelse
 {
    public static void main(String[] args)
    {
       int x = 15;
       int y = 7;


       if(x>y)
              {
                System.out.println("The greater number is x= " +x);
              }
        else
              {
                System.out.println("The greater number is y= " +y);
              }

    }
}
