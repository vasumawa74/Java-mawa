package practices;
public class even_odd
 {
    public static void main(String[] args)
    {
       int x = 15;

       if(x%2==0)  //remainder of x divided by 2 is 0
              {
                System.out.println("The even number is x= " +x);
              }
        else
              {
                System.out.println("The odd number is x= " +x);
              }

    }
}
